// Reverse a String
// Question: Write a function that takes a string as input and returns the string in reverse order.

function reverseString(str) {
    return str.split('').reverse().join('');
  }
  
  // Example usage:
  console.log(reverseString("hello")); // Output: "olleh"



// the split method 
// allows you to split the string into an array of substrings based on a given pattern. 

// The reverse() method 
// reverses the order of the elements in an array.

// The join() method 
// is an essential tool for converting arrays into strings.
  